// returns sum of x and y
function add(x, y) {
  return x + y;
}

// returns difference of x and y
function subtract(x, y) {
  return x - y;
}

// returns product of x and y
function multiply(x, y) {
  return x * y;
}

// returns quotient of x and y
function divide(x, y) {
  return x / y;
}